# Basic features of python
# Variables - a container for a value . Temporarily stores the data into the computer memory.

name = "Armir"

print(name)
print("Hello " + name) #concatenation of string

# other concatenation examples
first_name = "Armir"
last_name = "Halimi"
full_name = first_name + last_name

print(full_name)
print("Hello my name is " + full_name)


#Multiple assignment method, allows you to assign multiple variables at the same time in one line of code

name, age, attractive = "Armir", 21, True #only one line of code assignment of variables
print(name)
print(age)
print(attractive)

#or if multiple variables have the same value, example:

Spongebob = Patrick = Sandy = 30
print(Spongebob)
