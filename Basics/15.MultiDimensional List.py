#2D Lists = a list of lists

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza", "hamburger", "hot-dog"]
dessert = ["cake", "pudding", "ice-cream"]

food = [drinks,dinner,dessert] #create the 2d list
print(food) #print all the lists in one
print(food[0]) #access the first list only
print(food[0][0]) #access the element within the first list

