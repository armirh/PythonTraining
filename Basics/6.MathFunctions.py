#to use math functions we have to import math module

import math

pi = 3.14
x = 1
y = 2
z = 3


print(round(pi)) #round the number => 3.0
print(math.ceil(pi)) #round number up to the nearest number => 4
print(math.floor(pi)) #round number down => 3
print(abs(pi)) #absulte number of that variable -3.14 => 3.14
print(pow(pi,2)) #(var, the number we want to power this number to)
print(math.sqrt(420)) #square root of that number
print(max(x,y,z)) #find the highest number of those variables
print(min(x,y,z)) #find the lowest number